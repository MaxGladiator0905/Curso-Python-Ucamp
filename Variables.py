# Hay muchos tipos de variables en Python, los cuales nos van a ayudar a identificar los diferentes tipos de valores con los cuales podemos trabajar en nuestros programas 

# Aquí hay una variable de tipo entero
numero = 147
type(numero) #"type" sirve para escribir lo que esté encasillado
print(numero) # Ésta linea sirve para imprimir la variable número, gracias a que lleva la variable "print, luego type, y lo que está encasillado"
print(type(numero))

# Redefinimos la variable número.
numero = 147.5
print(numero)
print(type(numero))

complejo = 1 + 2j
print(complejo)
print(type(complejo))

palabra = "Hola"
print(palabra)
print(type(palabra))

bandera = True
print(bandera)
print(type(bandera))

Vacio = None
print(Vacio)
print(type(Vacio))

